#include <iostream>

using namespace std;

int main() {
    int num1 = 10;             // Integer constant 10
    int num2 = num1 + 10;      // Arithmetic assignment operator to generate 20
    int num3 = --num2;         // Decrement operator to generate 19

    // Output the numbers separated by commas
    
    cout << num1 << ", " << num2 << ", " << num3 << endl;
    

    return 0;
}
